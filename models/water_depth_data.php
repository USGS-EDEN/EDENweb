<h3><strong>Calling all those with water depth measurements in the Everglades:</strong></h3>
<p>The EDEN project is compiling water depth data collected by scientists and field personnel since 1990 to current. These data will be used to help test and improve the EDEN water surface model and the ground elevation model.</p>
<p>If you have data for use by EDEN, the data must be provided in an Excel spreadsheet (<a href="#wdtemplate">see template below</a>) and contain the following information to be considered for use by EDEN:</p>
<p><strong>Required:</strong></p>
<ul>
  <li>Describe your collection method and other significant metadata
    <ul>
      <li>For example, describe your equipment used; do you use a weighted footed rod to prevent deep penetration of the mucky bottom? Do you push through the sediment to bedrock?</li>
      <li>Do you measure an area repeatedly to obtain an average? Data for EDEN is individual measurements only, no averaged datasets.</li>
    </ul>
  </li>
  <li>Briefly describe the purpose of your data collection</li>
  <li>Location of measured site
    <ul>
      <li>Latitude and longitude or UTM coordinates</li>
      <li>Horizontal datum (i.e. NAD83 or other)</li>
    </ul>
  </li>
  <li>Date of measurement</li>
  <li>Collector name or id</li>
  <li>Measured water depth
    <ul>
      <li>Units (i.e. feet, centimeters)</li>
    </ul>
  </li>
</ul>
<p>Optional information can include:</p>
<ul>
  <li>Other parameters
    <ul>
      <li>Vegetation</li>
      <li>Soil</li>
      <li>Others (no water quality data please)</li>
    </ul>
  </li>
</ul>
<p>At this time, <strong>EDEN has no plans to post these data for public access and will never share or post your data without obtaining your permission</strong>. Thank you for sharing your data with us.</p>
<p>The EDEN team</p>
